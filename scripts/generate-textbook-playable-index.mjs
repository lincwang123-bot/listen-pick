import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";
import { textbookLevels } from "../src/course/textbook-levels-001-300.generated.mjs";

const outputPath = "src/course/textbook-playable.generated.mjs";

const availableTextbookLevels = textbookLevels.filter((level) =>
  level.questions.every(
    (question) =>
      existsSync(question.correctImage) &&
      existsSync(question.wrongImage) &&
      existsSync(question.audioFile)
  )
);

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  `// Generated by scripts/generate-textbook-playable-index.mjs.\n` +
    `// Includes only textbook levels whose cropped illustration and audio assets exist.\n\n` +
    `export const availableTextbookLevels = ${JSON.stringify(availableTextbookLevels, null, 2)};\n`
);

console.log(`Available textbook levels: ${availableTextbookLevels.map((level) => level.level).join(", ") || "none"}`);
